CREATE PROCEDURE ACERTA_ENDERECO
AS
DECLARE VARIABLE V_LOGRADOURO CHAR(60);
DECLARE VARIABLE V_BAIRRO CHAR(60);
DECLARE VARIABLE V_CIDADE CHAR(60);
DECLARE VARIABLE V_MUNIBGE CHAR(10);
DECLARE VARIABLE V_CEP CHAR(10);
DECLARE VARIABLE V_CODIGO INTEGER;
BEGIN

  FOR
  SELECT
    CLIENTES.CODIGO,
    LOGRADOUROS.LOGRADOURO,
    BAIRROS.BAIRRO,
    CIDADES.CIDADE,
    CIDADES.MUNIBGE,
    LOGRADOUROS.CEP
  FROM
    CLIENTES
    JOIN CIDADES ON
      (CIDADES.CODIGO = CLIENTES.CIDADE AND
       CIDADES.ESTADO = CLIENTES.ESTADO)
    JOIN BAIRROS ON
      (BAIRROS.CODIGO = CLIENTES.BAIRRO AND
       BAIRROS.CIDADE = CLIENTES.CIDADE AND
       BAIRROS.ESTADO = CLIENTES.ESTADO)
    JOIN LOGRADOUROS ON
      (LOGRADOUROS.CODIGO = CLIENTES.LOCALIZACAO AND
       LOGRADOUROS.BAIRRO = CLIENTES.BAIRRO AND
       LOGRADOUROS.CIDADE = CLIENTES.CIDADE AND
       LOGRADOUROS.ESTADO = CLIENTES.ESTADO)
  INTO
    :V_CODIGO,
    :V_LOGRADOURO,
    :V_BAIRRO,
    :V_CIDADE,
    :V_MUNIBGE,
    :V_CEP
  DO
  BEGIN

    UPDATE
      CLIENTES
    SET
      LOGRADOURO = :V_LOGRADOURO,
      BAIRRO1 = :V_BAIRRO,
      CIDADE1 = :V_CIDADE,
      MUNIBGE = :V_MUNIBGE,
      CEP = :V_CEP
    WHERE
      CODIGO = :V_CODIGO;

  END

END
^

